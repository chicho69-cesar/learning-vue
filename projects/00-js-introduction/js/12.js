// Iteradores en JS

const technologyList = ['Vue.js', 'JavaScript', 'Node.js', 'HTML', 'CSS', 'Git']

// forEach
const arrayForEach = technologyList.forEach(function (tech) {
  return tech
})

// map
const arrayMap = technologyList.map(function (tech) {
  return tech
})

console.log(arrayForEach)
console.log(arrayMap)
